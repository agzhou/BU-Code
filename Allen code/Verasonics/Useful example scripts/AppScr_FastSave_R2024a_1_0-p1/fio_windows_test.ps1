# Configuration
$targetPath = "D:\"           # Set this to your target drive or folder
$outputCsv = "nvme_write_benchmark.csv"
$FILE_SIZE_B = 2147483648
$BLOCK_SIZE="4M"
$RUNTIME_LIMIT=360  # Set to non-zero seconds to auto-stop after this time
$INTERVAL = 0.1


Write-Output "Starting $RUNTIME_LIMIT second Windows FIO test on drive: $targetPath for file size: $FILE_SIZE_B Bytes"

# --- Initialize Counters ---
$FileIndex = 0
$TotalWrittenGB = 0
$StartTime = Get-Date

# Make sure target directory exists
New-Item -ItemType Directory -Force -Path $targetPath | Out-Null

# Write CSV header
 "Time_Elapsed(s),Total_Data_Written(GB),Write_Speed(MB/s)" | Out-File -Encoding UTF8 $outputCsv

# Start time reference
$startTime = Get-Date

# Main loop
while ($true) {
    $FileIndex++
    $TotalWrittenGB = $TotalWrittenGB + [math]::Round($FILE_SIZE_B/1e9)
    $FileName = ("write_{0:D4}.dat" -f $FileIndex)
    $FilePath = Join-Path $targetPath $FileName
    $jsonOutput = Join-Path $targetPath "writeInfo.json"

    $Elapsed = (New-TimeSpan -Start $StartTime -End (Get-Date)).TotalSeconds

    & fio.exe --name=$FilePath `
        --size=$FILE_SIZE_B `
        --bs=$BLOCK_SIZE `
        --thread `
        --rw=write `
        --ioengine=windowsaio `
        --direct=1 `
        --verify=0 `
        --numjobs=1 `
        --iodepth=64 `
        --group_reporting=1 `
        --output=$jsonOutput `
        --output-format=json


    if (Test-Path $jsonOutput) {
        $content = Get-Content $jsonOutput -Raw
        if ($content.Trim().StartsWith("{")) {
            $json = $content | ConvertFrom-Json

            $bw_MBps = [math]::Round($json.jobs[0].write.bw_bytes / 1MB, 2)
            $relativeTime = [math]::Round((Get-Date).Subtract($startTime).TotalSeconds, 2)

            "$relativeTime, $TotalWrittenGB, $bw_MBps" | Out-File -Encoding UTF8 -Append $outputCsv

            Write-Output "write speed: $bw_MBps"
        } else {
            Write-Warning "$jsonOutput is not valid JSON. Skipping..."
        }
    } else {
        Write-Warning "$jsonOutput not found. Possibly a failed FIO run."
    }

    # Check runtime limit
    if ($RuntimeLimit -gt 0 -and $Elapsed -ge $RuntimeLimit) {
        Write-Host "Reached runtime limit. Exiting."
        break
    }

    Start-Sleep -Seconds $INTERVAL
}

Write-Host "Benchmark complete. Results saved to $OutputCsv"

